#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	int		i;

	if (!s)
		return ;
	i = 0;
	while (s[i] && s && f)
	{
		f(i, &s[i]);
		i++;
	}
}
